import uuid
from datetime import datetime

from sqlalchemy import Boolean, DateTime, String, func
from sqlalchemy.dialects.postgresql import UUID
from sqlalchemy.orm import Mapped, mapped_column, relationship

from app.core.database import Base


class AppUser(Base):
    __tablename__ = "app_users"

    id: Mapped[uuid.UUID] = mapped_column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    full_name: Mapped[str] = mapped_column(String(150), nullable=False)
    email: Mapped[str] = mapped_column(String(150), unique=True, nullable=False)
    password_hash: Mapped[str] = mapped_column(String(255), nullable=False)
    role: Mapped[str] = mapped_column(String(30), nullable=False)
    is_active: Mapped[bool] = mapped_column(Boolean, default=True, nullable=False)
    created_at: Mapped[datetime] = mapped_column(DateTime(timezone=True), server_default=func.now(), nullable=False)
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), server_default=func.now(), onupdate=func.now(), nullable=False
    )

    managed_initiatives = relationship("Initiative", back_populates="responsible_manager", foreign_keys="Initiative.responsible_manager_id")
    managed_assignments = relationship("ResourceAssignment", back_populates="manager", foreign_keys="ResourceAssignment.manager_id")
    analyst_assignments = relationship(
        "ResourceAssignment", back_populates="analyst_responsible", foreign_keys="ResourceAssignment.analyst_responsible_id"
    )
    import_batches = relationship("ImportBatch", back_populates="imported_by_user")
